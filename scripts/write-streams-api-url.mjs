#!/usr/bin/env node
/**
 * Bake SCOR_STREAMS_API_URL into electron/streamsApiUrl.ts before electron:build.
 * Safe to run without the env var (keeps the committed default).
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..')
const out = path.join(root, 'electron', 'streamsApiUrl.ts')
const fromEnv = (process.env.SCOR_STREAMS_API_URL || '').trim().replace(/\/$/, '')

if (!fromEnv) {
  console.log('[streams-api-url] SCOR_STREAMS_API_URL unset — keeping', out)
  process.exit(0)
}

const contents = `/**
 * Public streams proxy base URL (not a secret).
 * Generated by scripts/write-streams-api-url.mjs from SCOR_STREAMS_API_URL.
 */
export const STREAMS_API_URL = ${JSON.stringify(fromEnv)}
`

fs.writeFileSync(out, contents)
console.log('[streams-api-url] wrote', fromEnv, '→', out)
